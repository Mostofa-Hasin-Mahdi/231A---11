#include <bits/stdc++.h>
using namespace std;

int main(){
    
    int x,y;
    cin>>x>>y;

    int temp = x + y;
    int grtr, low;

    if(x == 1 && y == 1){
        cout<<1;
    }
    else{
        if(x>y){
        grtr = x;
        low = y;
    }
    else{
        grtr = y;
        low = x;
    }

    cout<<abs((temp - (grtr+1))+low);
    }

    return 0;
}